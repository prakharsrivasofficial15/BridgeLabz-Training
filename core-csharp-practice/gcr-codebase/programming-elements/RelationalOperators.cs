using System;

class RelationalOperators
{
    static void Main()
    {
        int a = 5;
        int b = 15;

        Console.WriteLine(a < b);   
        Console.WriteLine(a > b);   
        Console.WriteLine(a <= b);  
        Console.WriteLine(a >= b);  
        Console.WriteLine(a == b);  
        Console.WriteLine(a != b);  
    }
}
