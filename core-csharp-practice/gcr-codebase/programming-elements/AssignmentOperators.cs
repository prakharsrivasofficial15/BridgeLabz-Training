using System;

class AssignmentOperators{
	static void main(String[] args){
		
		int a = 15;
		int b = 3;
		
		Console.WriteLine("a=b: " + a=b);
		Console.WriteLine("a+=b: " + a+=b); // a = a + b
		Console.WriteLine("a-=b: " + a-=b); // a = a - b
		Console.WriteLine("a*=b: " + a*=b); // a = a * b
		Console.WriteLine("a/=b: " + a/=b); // a = a / b
		Console.WriteLine("a%=b: " + a%=b); // a = a % b
	}
}


